package com.fczx.tq.service.impl;

import com.baomidou.mybatisplus.core.conditions.query.QueryWrapper;
import com.baomidou.mybatisplus.core.toolkit.CollectionUtils;
import com.baomidou.mybatisplus.core.toolkit.ObjectUtils;
import com.baomidou.mybatisplus.extension.plugins.pagination.Page;
import com.baomidou.mybatisplus.extension.service.impl.ServiceImpl;
import com.fczx.tq.common.ErrorCode;
import com.fczx.tq.constant.GroupConstant;
import com.fczx.tq.exception.BusinessException;
import com.fczx.tq.judge.JudgeService;
import com.fczx.tq.model.dto.questSubmit.QuestionSubmitAddRequest;
import com.fczx.tq.model.dto.questSubmit.QuestionSubmitQueryRequest;
import com.fczx.tq.model.entity.Question;
import com.fczx.tq.model.entity.QuestionSubmit;
import com.fczx.tq.model.entity.User;
import com.fczx.tq.model.enums.QuestionSubmitLanguageEnum;
import com.fczx.tq.model.enums.QuestionSubmitStatusEnum;
import com.fczx.tq.model.vo.QuestionSubmitVO;
import com.fczx.tq.service.QuestionService;
import com.fczx.tq.service.QuestionSubmitService;
